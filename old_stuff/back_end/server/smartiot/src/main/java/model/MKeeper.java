package model;

/**
 * Keeper
 */
public class MKeeper {
    private String Kid;
    private int age;

    /**
     * @return the kid
     */
    public String getKid() {
        return Kid;
    }

    /**
     * @param kid the kid to set
     */
    public void setKid(String kid) {
        this.Kid = kid;
    }

    /**
     * @return the age
     */
    public int getAge() {
        return age;
    }

    /**
     * @param age the age to set
     */
    public void setAge(int age) {
        this.age = age;
    }
}